/** The class RegularShapedProduct 
 *  represents a regular-shaped
 *  product object with its length,
 *  width, height, and actual weight.
 * 
 *  @author Isser Troy M. Gagan
 *  @version 1.0
 */

public class RegularShapedProduct extends Product
{
    /** This constructor initializes all the
     *  attributes of the regular-shaped product object.
     * 
     *    @param length the length of the regular-shaped product
     *    @param width the width of the regular-shaped product
     *    @param height the height of the regular-shaped product
     *    @param actualWeight the actual weight of the regular-shaped product
     */
    public RegularShapedProduct(double length, double width, double height, double actualWeight)
    {
        super(length, width, height, actualWeight);
    }
}